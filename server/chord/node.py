import logging

class Node:
    def __init__():
        pass

    def get_key(self, key: str) -> str:
        logging.info(f'Get key {key}')

        return ''

    def set_key(self, key: str, value: str) -> bool:
        logging.info(f'Set key {key} with value {value}')

        return 0

    def remove_key(self, key: str) -> bool:
        logging.info(f'Remove key {key}')

        return 0